package com.dollarsbank.utility;

//NOTE: I am using the "ANSI Escape in Console" plugin from the Eclipse marketplace
//This plugin interprets the ANSI escape sequences in my code to color the console output as desired.
public class ColorsUtility {
	public static final String ANSI_DEFAULT = "\u001B[0m"; //to reset color to normal console color
	public static final String ANSI_RED = "\u001B[31m"; //for red text
	public static final String ANSI_GREEN = "\u001B[32m"; //for green text
	public static final String ANSI_BLUE = "\u001B[34m"; //for blue text
	public static final String ANSI_CYAN = "\u001b[36m"; //for cyan text
}
